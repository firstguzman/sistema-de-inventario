                        //update precio
                        pst = cm.prepareStatement("UPDATE producto SET precioTotal = (? + ?) * ? WHERE nombre = ?");
                        pst.setInt(1, cantidadInsertadaInt);
                        pst.setInt(2, cantidadProducto);
