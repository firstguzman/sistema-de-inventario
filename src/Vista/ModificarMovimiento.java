                        //update cantidad
                        pst = cm.prepareStatement("UPDATE producto SET cantidad = cantidad - ? + ? WHERE nombre = ?");
                        pst.setInt(1, cantidadMovimientoModificado);
                        pst.setString(2, cantidadInsertada);
                        pst.setString(3, nombreProducto);
