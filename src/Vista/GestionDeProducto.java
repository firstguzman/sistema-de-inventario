    private void tableUpdateProductos(){
        try {
            //seleccionar todo de la tabla categoria
            pst = cm.prepareStatement("SELECT * FROM producto");
            ResultSet rs = pst.executeQuery();
            //creamos tabla modelo
            DefaultTableModel dataTable = (DefaultTableModel) jTableProductos.getModel();
            // iterar por el resulset
            while (rs.next()) {
                dataTable.addRow(new Object[]{rs.getString("categoria"), rs.getInt("id"), rs.getString("nombre"), rs.getInt("cantidad")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(GestionDeCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }         
    }
    
    private void tableUpdateProductosBajoStock(){
        try {
            //seleccionar todo de la tabla categoria
            pst = cm.prepareStatement("SELECT * FROM producto");
            ResultSet rs = pst.executeQuery();
            //creamos tabla modelo
            DefaultTableModel dataTable = (DefaultTableModel) jTableProductosBajoStock.getModel();
            // iterar por el resulset
            while (rs.next()) {
                if (rs.getInt("cantidad") < 11) {
                    dataTable.addRow(new Object[]{rs.getString("categoria"), rs.getInt("id"), rs.getString("nombre"), rs.getString("cantidad")});
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(GestionDeCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void tableUpdateProductosFueraStock(){
        try {
            //seleccionar todo de la tabla categoria
            pst = cm.prepareStatement("SELECT * FROM producto");
            ResultSet rs = pst.executeQuery();
            //creamos tabla modelo
            DefaultTableModel dataTable = (DefaultTableModel) jTableProductosFueraStock.getModel();
            // iterar por el resulset
            while (rs.next()) {
                if (rs.getInt("cantidad") == 0) {
                    dataTable.addRow(new Object[]{rs.getString("categoria"), rs.getInt("id"), rs.getString("nombre"), "AGOTADO"});
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(GestionDeCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }
    
  

