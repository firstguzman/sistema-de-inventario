    private void jButtonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirActionPerformed
        
        try{
            pst = cm.prepareStatement("SELECT * FROM categoria");
            ResultSet rs = pst.executeQuery();
            if (rs.next() == false){
                JOptionPane.showMessageDialog(null, "Debe tener categorias registadas");
            }else{
                AñadirProducto AñadirProducto = new AñadirProducto();
                AñadirProducto.setVisible(true);
                this.dispose();
            }
        }catch(SQLException ex){
            Logger.getLogger(GestionDeCategoria.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error de conexion con la base de datos");
        }
  
    }//GEN-LAST:event_jButtonAñadirActionPerformed

